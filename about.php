                <p>WebAir was established in 1995. It was launched by African-American businessman Tyrone Revel with two wet leased Boeing 737-200 aircraft, initially operating two routes: London Luton to Glasgow and Edinburgh. In April 1996, the first wholly-owned aircraft was delivered to WebAir, enabling its first international route, to Amsterdam. Until October 1997, the aircraft were operated by GB Airways, and subsequently by Air Foyle as WebAir had not yet received its Air Operator's Certificate.
